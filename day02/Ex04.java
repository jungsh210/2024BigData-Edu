public class Ex04 {
    public static void main(String[] args){
        System.out.println(3>5);
        System.out.println(3<=5);
        System.out.println(3>=5);
        System.out.println(3<5);
        System.out.println(3==5); // ==: 3과 5가 같냐는 의미
        System.out.println(3!=5); // != : 3과 5가 같지 않냐는 의미
    }
    
}
